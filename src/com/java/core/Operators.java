package com.java.core;

public class Operators {
	
	public static void main(String[] args) {
		 
		int a = 20;
		int b = 11;
		
		int sum = a+b;
		System.out.println(sum);
		
		int subs = a-b;
		System.out.println(subs);
		
		int mul = a*b;
		System.out.println(mul);
		
		int div = a/b;
		System.out.println(div);
		
		int rem = a%b; 
		System.out.println(rem);
		
		
		
		
	}

}
